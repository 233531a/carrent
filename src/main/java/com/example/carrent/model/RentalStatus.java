package com.example.carrent.model;

/**
 * Перечисление статусов аренды автомобиля.
 *
 * Определяет текущее состояние бронирования/аренды:
 * - PENDING: заявка создана и ожидает одобрения менеджером
 * - ACTIVE: аренда активна, автомобиль используется клиентом
 * - REJECTED: заявка отклонена менеджером
 * - COMPLETED: аренда завершена, автомобиль возвращен
 * - CANCELLED: бронирование отменено клиентом
 *
 * Статусы PENDING и ACTIVE блокируют автомобиль (available = false).
 * Остальные статусы освобождают автомобиль для других бронирований.
 *
 * @author Система аренды автомобилей
 * @version 1.0
 * @since 2025-01-25
 */
public enum RentalStatus {
    /**
     * Ожидание одобрения.
     * Заявка создана клиентом и ожидает подтверждения менеджером.
     */
    PENDING("Ожидание"),

    /**
     * Активная аренда.
     * Аренда одобрена, автомобиль выдан клиенту и используется.
     */
    ACTIVE("Активна"),

    /**
     * Отклонена.
     * Менеджер отклонил заявку на аренду.
     */
    REJECTED("Отклонена"),

    /**
     * Завершена.
     * Аренда успешно завершена, автомобиль возвращен.
     */
    COMPLETED("Завершена"),

    /**
     * Отменена.
     * Бронирование отменено клиентом до начала аренды.
     */
    CANCELLED("Отменена");

    /**
     * Русскоязычное название статуса для отображения в интерфейсе.
     */
    private final String titleRu;

    /**
     * Конструктор статуса аренды.
     *
     * @param titleRu русскоязычное название статуса
     */
    RentalStatus(String titleRu) {
        this.titleRu = titleRu;
    }

    /**
     * Получить русскоязычное название статуса.
     *
     * @return название статуса на русском языке
     */
    public String getTitleRu() {
        return titleRu;
    }
}
