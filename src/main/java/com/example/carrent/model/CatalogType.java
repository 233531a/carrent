package com.example.carrent.model;

/**
 * Перечисление типов каталогов для автомобилей.
 *
 * Определяет, в каком разделе каталога отображается автомобиль.
 * Используется для разделения автомобилей по назначению:
 * - обычные автомобили для аренды клиентам
 * - специальные автомобили для такси
 * - автомобили для доставки
 *
 * @author Система аренды автомобилей
 */
public enum CatalogType {
    /**
     * Обычный каталог автомобилей.
     * Доступен всем пользователям для бронирования.
     */
    REGULAR,

    /**
     * Специальный каталог автомобилей для такси.
     * Доступен только сотрудникам, менеджерам и администраторам.
     */
    TAXI,

    /**
     * Специальный каталог автомобилей для доставки.
     * Доступен только сотрудникам, менеджерам и администраторам.
     */
    DELIVERY
}
